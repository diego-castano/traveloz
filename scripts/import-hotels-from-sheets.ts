/**
 * import-hotels-from-sheets.ts — Reads the client's public Google Sheets
 * (one doc per country, one tab per city) and produces an import plan for
 * the Alojamiento table.
 *
 * Modes:
 *   (no flag)    dry-run. Prints breakdown + anomalies, no DB writes.
 *   --confirm    creates missing Ciudades and inserts new Alojamiento rows
 *                in parallel to existing hotels. Does NOT delete the 274
 *                existing hotels (that happens in migrate-hotels.ts).
 *
 * Optional:
 *   --snapshot-output=<path>   writes the parsed hotel plan as JSON
 *
 * The script never touches Region / Pais / Ciudad beyond creating missing
 * cities from the Sheets.
 */

