    const key = `${h.pais}|${normalizeForMatch(h.ciudadNombre)}`;
    if (seen.has(key)) continue;
    seen.add(key);
    if (!dbByKey.has(key)) missing.push({ pais: h.pais, ciudadNombre: h.ciudadNombre });
