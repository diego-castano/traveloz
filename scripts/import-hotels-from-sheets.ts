async function main() {
  const args = process.argv.slice(2);
  const confirm = args.includes('--confirm');
  const snapshotArg = args.find((a) => a.startsWith('--snapshot-output='));

  console.log('📖 Reading hotel sheets from client docs…');
  const plan = await buildImportPlan();

  if (snapshotArg) {
    const path = snapshotArg.split('=', 2)[1];
    writeFileSync(path, JSON.stringify(plan.hotels, null, 2));
    console.log(`   ✓ Snapshot written to ${path}`);
  }

  if (!confirm) {
    await printReport(plan);
    return;
  }

  await printReport(plan);
  console.log();
  console.log('⚠️  --confirm flag detected. Persisting to DB in 3 seconds…');
  await new Promise((r) => setTimeout(r, 3000));
  await persist(plan);
  console.log('✅ Done.');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
