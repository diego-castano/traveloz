// ---------------------------------------------------------------------------
// CSV parser — handles quoted fields with doubled-quote escaping.
// Google Sheets CSV output is well-formed so this minimal parser is enough.
// ---------------------------------------------------------------------------

function parseCSV(text: string): string[][] {
  const rows: string[][] = [];
  let row: string[] = [];
  let field = '';
  let inQuotes = false;

  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') {
          field += '"';
          i++;
        } else {
          inQuotes = false;
        }
      } else {
        field += c;
      }
    } else {
      if (c === '"') {
        inQuotes = true;
      } else if (c === ',') {
        row.push(field);
        field = '';
      } else if (c === '\n') {
        row.push(field);
        rows.push(row);
        row = [];
        field = '';
      } else if (c === '\r') {
        // skip, handle on \n
      } else {
        field += c;
      }
    }
  }
  if (field.length > 0 || row.length > 0) {
    row.push(field);
    rows.push(row);
  }
  return rows;
}

// ---------------------------------------------------------------------------
// Normalisation helpers
// ---------------------------------------------------------------------------

function normalizeName(s: string): string {
  return s.trim().toLowerCase().replace(/\s+/g, ' ');
}

function normalizeForMatch(s: string): string {
  return s
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .toLowerCase()
    .replace(/\s+/g, ' ')
    .trim();
}

function parseCategoria(raw: string): {
  categoria: number | null;
  categoriaTexto: string | null;
} {
  const value = raw.trim();
  if (!value) return { categoria: null, categoriaTexto: null };
  if (value.toUpperCase() === 'CATEGORIA') return { categoria: null, categoriaTexto: null }; // junk header

  // Pure N* (e.g. "3*", "4*", "5*", "2*")
  const pure = /^([1-5])\s*\*$/.exec(value);
  if (pure) return { categoria: Number(pure[1]), categoriaTexto: null };

  // Hybrid e.g. "4*SUP", "3* - Básico", "3* - Super Básico"
  const hybrid = /^([1-5])\s*\*/.exec(value);
  if (hybrid) return { categoria: Number(hybrid[1]), categoriaTexto: value };

  // Pure text — "Básica", "Media", "Sup", "Superior", "Media Sup", etc.
  return { categoria: null, categoriaTexto: value };
}

