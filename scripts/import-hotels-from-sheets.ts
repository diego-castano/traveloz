interface ParsedHotel {
  pais: string;
  tabName: string;       // original tab name from the sheet
  ciudadNombre: string;  // resolved city name after overrides (matches DB)
  nombre: string;
  categoria: number | null;
  categoriaTexto: string | null;
  comentarios: string | null;
  ubicacion: string | null;
  aclaracion: string | null;
}

interface ImportPlan {
  hotels: ParsedHotel[];
  perTab: Array<{ pais: string; tabName: string; ciudadNombre: string; count: number }>;
  discardedRows: number;
  duplicatesCollapsed: number;
  categoriaBreakdown: Record<string, number>;
  uniqueTextCategorias: Set<string>;
}

// ---------------------------------------------------------------------------
// Fetch + parse one tab
// ---------------------------------------------------------------------------

async function fetchTab(docId: string, tab: string): Promise<string[][]> {
  const url = `https://docs.google.com/spreadsheets/d/${docId}/gviz/tq?tqx=out:csv&sheet=${encodeURIComponent(tab)}`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Failed to fetch ${tab} from ${docId}: HTTP ${res.status}`);
  const text = await res.text();
  return parseCSV(text);
}

function extractHotelsFromTab(
  rows: string[][],
  pais: string,
  tabName: string,
  ciudadNombre: string,
): { hotels: ParsedHotel[]; discarded: number } {
  // Find header row (contains "HOTEL")
  let headerIdx = -1;
  for (let i = 0; i < Math.min(rows.length, 10); i++) {
    if (rows[i]?.some((c) => (c ?? '').trim().toUpperCase() === 'HOTEL')) {
      headerIdx = i;
      break;
    }
  }
  if (headerIdx === -1) return { hotels: [], discarded: 0 };

  const header = rows[headerIdx].map((c) => (c ?? '').trim().toUpperCase());
  const idx = {
    HOTEL: header.indexOf('HOTEL'),
    CATEGORIA: header.indexOf('CATEGORIA'),
    COMENTARIOS: header.indexOf('COMENTARIOS'),
    UBICACION: header.indexOf('UBICACIÓN') === -1
      ? header.indexOf('UBICACION')
      : header.indexOf('UBICACIÓN'),
    ACLARACION: header.indexOf('ACLARACIÓN EXTRA') === -1
      ? header.indexOf('ACLARACION EXTRA')
      : header.indexOf('ACLARACIÓN EXTRA'),
  };

  const out: ParsedHotel[] = [];
  let discarded = 0;

  for (let i = headerIdx + 1; i < rows.length; i++) {
    const r = rows[i];
    const nameRaw = (r[idx.HOTEL] ?? '').trim();
    if (!nameRaw) continue;

    // Neighborhood markers (e.g. "PALERMO:") and section headers
    if (nameRaw.endsWith(':')) {
      discarded++;
      continue;
    }

    // City-section header inside a multi-city tab (e.g. "MENDOZA", "BARILOCHE"
    // inside the Argentina Buenos Aires tab). Heuristic: all-uppercase word(s),
    // no category, no comments.
    const categoriaRaw = (r[idx.CATEGORIA] ?? '').trim();
    const isAllCaps = nameRaw === nameRaw.toUpperCase() && /[A-ZÁÉÍÓÚÑ]/.test(nameRaw);
    const looksLikeSectionHeader =
      isAllCaps &&
      !categoriaRaw &&
      !/\d/.test(nameRaw) &&
      nameRaw.length > 3 &&
      nameRaw.length < 50;
    if (looksLikeSectionHeader) {
      discarded++;
      continue;
    }

    // Header duplicates — rows where col B is literally "CATEGORIA"
    if (categoriaRaw.toUpperCase() === 'CATEGORIA') {
      discarded++;
      continue;
    }

    // Strip trailing " 3*" / " 4*" etc. from name if present
    const cleanName = nameRaw.replace(/\s+[1-5]\*(?:SUP)?(?:\s*-\s*[A-Za-zÁÉÍÓÚáéíóúÑñ ]+)?\s*$/, '').trim();

    const { categoria, categoriaTexto } = parseCategoria(categoriaRaw);

    out.push({
      pais,
      tabName,
      ciudadNombre,
      nombre: cleanName,
      categoria,
      categoriaTexto,
      comentarios: idx.COMENTARIOS >= 0 ? (r[idx.COMENTARIOS] ?? '').trim() || null : null,
      ubicacion: idx.UBICACION >= 0 ? (r[idx.UBICACION] ?? '').trim() || null : null,
      aclaracion: idx.ACLARACION >= 0 ? (r[idx.ACLARACION] ?? '').trim() || null : null,
    });
  }

  return { hotels: out, discarded };
}

// ---------------------------------------------------------------------------
// Build import plan (fetch + parse + dedup, no DB writes)
// ---------------------------------------------------------------------------

export async function buildImportPlan(): Promise<ImportPlan> {
  const all: ParsedHotel[] = [];
  const perTab: ImportPlan['perTab'] = [];
  let totalDiscarded = 0;

  for (const [pais, src] of Object.entries(SHEET_SOURCES)) {
    for (const tab of src.tabs) {
      const override = CITY_NAME_OVERRIDES[pais]?.[tab];
      const ciudadNombre = override ?? tab;
      const rows = await fetchTab(src.docId, tab);
      const { hotels, discarded } = extractHotelsFromTab(rows, pais, tab, ciudadNombre);
      all.push(...hotels);
      totalDiscarded += discarded;
      perTab.push({ pais, tabName: tab, ciudadNombre, count: hotels.length });
    }
  }

  // Dedup by (normalizedName, ciudadNombre, pais)
  const seen = new Map<string, ParsedHotel>();
  let collapsed = 0;
  for (const h of all) {
    const key = `${h.pais}|${h.ciudadNombre}|${normalizeName(h.nombre)}`;
    if (seen.has(key)) collapsed++;
    seen.set(key, h); // last wins
  }
  const deduped = Array.from(seen.values());

  // Category histogram
  const categoriaBreakdown: Record<string, number> = {};
  const uniqueTextCategorias = new Set<string>();
  for (const h of deduped) {
    const label =
      h.categoria !== null && h.categoriaTexto === null
        ? `${h.categoria}*`
        : h.categoria !== null
        ? `${h.categoria}* (${h.categoriaTexto})`
        : h.categoriaTexto
        ? `text: ${h.categoriaTexto}`
        : '(empty)';
    categoriaBreakdown[label] = (categoriaBreakdown[label] ?? 0) + 1;
    if (h.categoria === null && h.categoriaTexto) uniqueTextCategorias.add(h.categoriaTexto);
  }

  return {
    hotels: deduped,
    perTab,
    discardedRows: totalDiscarded,
    duplicatesCollapsed: collapsed,
    categoriaBreakdown,
    uniqueTextCategorias,
  };
}

